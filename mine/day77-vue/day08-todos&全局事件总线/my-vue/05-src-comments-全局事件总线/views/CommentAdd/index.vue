<template>
  <div class="col-md-4">
    <form class="form-horizontal" @submit.prevent="addComment">
      <div class="form-group">
        <label>用户名</label>
        <input
          type="text"
          class="form-control"
          placeholder="用户名"
          v-model="name"
        />
      </div>
      <div class="form-group">
        <label>评论内容</label>
        <textarea
          class="form-control"
          rows="6"
          placeholder="评论内容"
          v-model="content"
        ></textarea>
      </div>
      <div class="form-group">
        <div class="col-sm-offset-2 col-sm-10">
          <button type="submit" class="btn btn-default pull-right">提交</button>
        </div>
      </div>
    </form>
  </div>
</template>
<script>
/*
    1.点击提交，触发addComment事件
  */
export default {
  name: "CommentAdd",
  data() {
    return {
      name: "",
      content: "",
    };
  },
  methods: {
    addComment() {
      /*
        1.如果有文本框为空，弹窗并停止程序
      */
      if (!this.name || !this.content) {
        alert("输入不能为空");
        return;
      }
      const { name, content } = this;
      this.$bus.$emit("addComment", name, content);
      // 添加完之后把文本置空
      this.name = "";
      this.content = "";
    },
  },
};
</script>
